package L002_DisplayNumbersInConsole;

public class DisplayNumbersInConsole {

        public static void main(String[] args) {

            System.out.println("Display the numbers 1 to 10 in the console");
            for (int a = 1; a <= 10; a++) {
                System.out.print(a +" ");
            } System.out.println("\n");

        }
}
